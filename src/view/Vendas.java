    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) Tabela_Produto.getModel();
        modelo.setNumRows(0);
        ProdutoDao cdao = new ProdutoDao();

        for (ProdutosBean p : cdao.read()) {

            modelo.addRow(new Object[]{
                p.getCodigo(),
                p.getNome(),
                p.getPreco(),
                p.getQtd(),
                p.getCat(),
                p.getForn(),
                p.getMarca(),
            });

        } 

    }
public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) Tabela_Produto.getModel();
        modelo.setNumRows(0);
        ProdutoDao cdao = new ProdutoDao();

        for (ProdutosBean p : cdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getCodigo(),
                p.getNome(),
                p.getPreco(),
                p.getQtd(),
                p.getCat(),
                p.getForn(),
                p.getMarca(),
            });

        }

    }
