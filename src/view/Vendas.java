    private void ComprarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ComprarMouseClicked
        // TODO add your handling code here:
        int Qtde = Integer.parseInt(estoque.getText()) - Integer.parseInt(Text_7.getText());
            estoque.setText(String.valueOf(Qtde));
        if (Tabela_Produto.getSelectedRow() != -1) {
            Text_2.setText(Tabela_Produto.getValueAt(Tabela_Produto.getSelectedRow(), 2).toString());
        ProdutosBean p = new ProdutosBean();
        ProdutoDao dao = new ProdutoDao();
        p.setNome(Text_1.getText());        
        p.setPreco(Double.parseDouble(Text_2.getText()));
        p.setQtd(Integer.parseInt(estoque.getText()));
        p.setCat(Text_4.getText());
        p.setForn(Text_5.getText());
        p.setMarca(Text_6.getText());
        p.setCodigo((int)Tabela_Produto.getValueAt(Tabela_Produto.getSelectedRow(), 0));
        
        dao.updateC(p);
        
        

        Text_1.setText("");
        Text_2.setText("");
        estoque.setText("");
        Text_4.setText("");
        Text_5.setText("");
        Text_6.setText("");
        Text_7.setText("");
        Total_T1.setText("");
        Total_troco.setText("");

        readJTable();

        }

    }//GEN-LAST:event_ComprarMouseClicked

